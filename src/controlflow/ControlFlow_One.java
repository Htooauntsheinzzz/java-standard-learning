package controlflow;

public class ControlFlow_One {
// Switch Statement - Condition Statement
	public static void main(String[] args) {
		
		
		int age = 21;
		
		
		switch (age) {
		
		
		case 10 : System.out.println("Your Age is 10");break;
		
		case 20 : System.out.println("Your Age is 20");break;
		
		case 15 : System.out.println("Your Age is 15");break;
		
		case 18 : System.out.println("Your Age is 18");break;
		
		default : System.out.println("I dont know your age");
		
		
		}
		
		
		

	}

}
