package controlflow;

public class Controlflow_Three {
// IF Statement
	
/**
 * 
 * 		if (condition ) {
 * 			statement 
 *  }
 */
	public static void main(String[] args) {
		
		
	 int age = 20;
	 
	 if (age  == 20) {  // Boolean Condition Checking 
		 System.out.println("Age is 20");
	 }
	 
	 
	 
	 String name = "Mg Mg";
	 
	 if (name.equals("Mg Mg")) { // Boolean Condition Checking
		 System.out.println("Here It is Mg mg");
		 
	 }
	

	}

}
