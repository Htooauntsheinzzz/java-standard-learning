package single_inheritance;

public class Person {
	
	
	
	String name;
	
	int age;
	
	String hairColor;
	
	String nrcNo;
	
	
	public Person() {
		
	}
	
	
	public void getPersonInfo() {
		
		System.out.println("Name is "+name);
		System.out.println("Age is "+age);
		System.out.println("Hair Color is "+hairColor);
		System.out.println("Nationality Number is "+nrcNo);
		
	}

}
